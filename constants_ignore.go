// https://docs.oracle.com/javase/specs/jvms/se8/html/jvms-4.html#jvms-4.1-200-E.1
type ClassAccessFlag uint16

const (
	// Declared public; may be accessed from outside its package.
	ACC_PUBLIC ClassAccessFlag = 0x0001
	// Declared final; no subclasses allowed.
	ACC_FINAL = 0x0010
	// Treat superclass methods specially when invoked by the invokespecial instruction.
	ACC_SUPER = 0x0020
	// Is an interface, not a class.
	ACC_INTERFACE = 0x0200
	// Declared abstract; must not be instantiated.
	ACC_ABSTRACT = 0x0400
	// Declared synthetic; not present in the source code.
	ACC_SYNTHETIC = 0x1000
	// Declared as an annotation type.
	ACC_ANNOTATION = 0x2000
	// Declared as an enum type.
	ACC_ENUM = 0x4000
)

// https://docs.oracle.com/javase/specs/jvms/se8/html/jvms-4.html#jvms-4.5-200-A.1
type FieldAccessFlag uint16

const (
	// Declared public; may be accessed from outside its package.
	ACC_PUBLIC FieldAccessFlag = 0x0001
	// Declared private; usable only within the defining class.
	ACC_PRIVATE = 0x0002
	// Declared protected; may be accessed within subclasses.
	ACC_PROTECTED = 0x0004
	// Declared static.
	ACC_STATIC = 0x0008
	// Declared final; never directly assigned to after object construction (JLS ยง17.5).
	ACC_FINAL = 0x0010
	// Declared volatile; cannot be cached.
	ACC_VOLATILE = 0x0040
	// Declared transient; not written or read by a persistent object manager.
	ACC_TRANSIENT = 0x0080
	// Declared synthetic; not present in the source code.
	ACC_SYNTHETIC = 0x1000
	// Declared as an element of an enum.
	ACC_ENUM = 0x4000
)

// https://docs.oracle.com/javase/specs/jvms/se8/html/jvms-4.html#jvms-4.6-200-A.1
type MethodAccessFlag uint16

const (
	// Declared public; may be accessed from outside its package.
	ACC_PUBLIC MethodAccessFlag = 0x0001
	// Declared private; accessible only within the defining class.
	ACC_PRIVATE = 0x0002
	// Declared protected; may be accessed within subclasses.
	ACC_PROTECTED = 0x0004
	// Declared static.
	ACC_STATIC = 0x0008
	// Declared final; must not be overridden (ยง5.4.5).
	ACC_FINAL = 0x0010
	// Declared synchronized; invocation is wrapped by a monitor use.
	ACC_SYNCHRONIZED = 0x0020
	// A bridge method, generated by the compiler.
	ACC_BRIDGE = 0x0040
	// Declared with variable number of arguments.
	ACC_VARARGS = 0x0080
	// Declared native; implemented in a language other than Java.
	ACC_NATIVE = 0x0100
	// Declared abstract; no implementation is provided.
	ACC_ABSTRACT = 0x0400
	// Declared strictfp; floating-point mode is FP-strict.
	ACC_STRICT = 0x0800
	// Declared synthetic; not present in the source code.
	ACC_SYNTHETIC = 0x1000
)

